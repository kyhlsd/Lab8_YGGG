import UIKit

extension UIColor {
    ///Custom Peach Color(red: 255, green: 212, blue: 178)
    static var yggg_peach: UIColor {
        return UIColor(red: 255/255, green: 212/255, blue: 178/255, alpha: 1.0)
    }
    
    ///Custom Gray Color(red: 248, green: 245, blue: 245)
    static var yggg_gray: UIColor {
        return UIColor(red: 248/255, green: 245/255, blue: 245/255, alpha: 1.0)
    }
    
    ///Custom Yello Color(red: 255, green: 246, blue: 189)
    static var yggg_yellow: UIColor {
        return UIColor(red: 255/255, green: 246/255, blue: 189/255, alpha: 1.0)
    }
    
    ///Custom Green Color(red: 206, green: 237, blue: 199)
    static var yggg_green: UIColor {
        return UIColor(red: 206/255, green: 237/255, blue: 199/255, alpha: 1.0)
    }
    
    ///Custom Teal Color(red: 134, green: 200, blue: 188)
    static var yggg_teal: UIColor {
        return UIColor(red: 134/255, green: 200/255, blue: 188/255, alpha: 1.0)
    }
    
    
}
